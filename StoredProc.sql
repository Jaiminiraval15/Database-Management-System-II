--------------------------------------------------
3.CREATE PROCEDURE PR_MST_CITY_UPDATE 
(	@CITYID INT,
	@CITYNAME VARCHAR(250),
	@PINCODE VARCHAR(6),
	@CITYREMARKS VARCHAR(500),
	@STATEID INT
	)
AS
BEGIN 
UPDATE MST_CITY
SET CITYID=@CITYID,
	CITYNAME=@CITYNAME,
	PINCODE=@PINCODE ,
	CITYREMARKS=@CITYREMARKS,
	STATEID=@STATEID
	
WHERE CITYID=@CITYID
END
