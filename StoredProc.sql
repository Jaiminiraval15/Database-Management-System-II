2.CREATE PROCEDURE  PR_MST_CITY_INSERT
(	@CITYID INT,
	@CITYNAME VARCHAR(250),
	@PINCODE VARCHAR(6),
	@CITYREMARKS VARCHAR(500),
	@STATEID INT
	)
AS
BEGIN
INSERT INTO MST_CITY(
	CITYID,
	CITYNAME,
	PINCODE ,
	CITYREMARKS,
	STATEID)
VALUES(
	@CITYID,
	@CITYNAME,
	@PINCODE,
	@CITYREMARKS,
	@STATEID
)
END
